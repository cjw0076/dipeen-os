def build_workspace_files(role: str, persona_key: str) -> dict[str, str]:
    """에이전트 workspace에 주입할 3개 파일 내용을 반환한다."""
    identity_tmpl = IDENTITY_TEMPLATES.get(persona_key, IDENTITY_TEMPLATES["coder"])
    soul_tmpl = SOUL_TEMPLATES.get(persona_key, SOUL_TEMPLATES["coder"])
    return {
        "IDENTITY.md": identity_tmpl.replace("{ROLE}", role),
        "SOUL.md": soul_tmpl,
        "AGENTS.md": AGENTS_MD,
    }
